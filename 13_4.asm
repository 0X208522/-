
;		13.4		BIOS和DOS所提供的中断例程（中断处理程序）


;在系统板的ROM中存放着一套程序，称为BIOS（基本输入输出系统），BIOS中主要包含以下几部分:
;(1)硬件系统的检测和初始化程序
;(2)外部中断（15章讲解）和内部中断的中断例程
;(3)用于对硬件设备进行I/O操作的中断例程
;(4)其他和硬件系统相关的中断例程


;操作系统DOS也提供了中断例程，从操作系统的角度看，DOS的中断例程就是操作系统向程序提供的
;编程资源

;BIOS和DOS在所提供的中断例程中包含了许多子程序，这些子程序实现了程序员在编程时经常用到
;的功能。程序员在编程的时候，可以用int指令直接调用BIOS和DOS提供的中断例程，来完成某些
;工作。和硬件设备相关的DOS中断例程中，一般都调用了BIOS的中断例程。


